nomer = [3,6,23,4,22,11,44,16]

#len untuk mengetahui berapa panjangnya dari list (ada berapa item di list tsb)
print(len(nomer))
print()

#max untuk mengetahui berapa yang terbesar
print(max(nomer))
print()

#min untuk mengetahui berapa yang terkecil
print(min(nomer))
print()

#sum untuk menjumlah yg ad di list
print(sum(nomer))
print()

#fungsi bisa dikombinasikan dengan fungsi lainnya
print(sum(nomer)/len(nomer))
print()