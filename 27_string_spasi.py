sapa = '      Halo bang     '
#lstrip akan menghapus spasi di sisi kiri
ubah = sapa.lstrip()
print(ubah)
print()

#rstrip akan menghapus spasi di sisi kanan
ubah_lagi = sapa.rstrip()
print(ubah_lagi)
print()

#strip akan menghapus spasi di kedua sisi
ubah_lagi_1 = sapa.strip()
print(ubah_lagi_1)
print()